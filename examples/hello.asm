; Hello World for 6809 - prints via ACIA TX at 0xA001
; asm6809.exe hello.asm -H -9 -o hello.hex
; asm6809.exe hello.asm -S -9 -o hello.s19
        ORG $1234
        LDX #DATA        ; X -> pointer to string
LOOP    LDA ,X+            ; A = *X; X++
        CMPA #$00          ; end on null terminator
        BEQ DONE
        STA $A001          ; send to ACIA at 0xA001
        BRA LOOP
DONE    
        SWI                 ; halt / exit
